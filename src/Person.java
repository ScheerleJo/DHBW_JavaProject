public class Person {
    private int id;
    private String name;
    private Role role;


    public void setId(int id) {
        this.id = id;
    }
    public void setName(String name) {
        this.name = name;
    }

    public Role getRole() {
        return role;
    }
    public int getId() {
        return id;
    }
    public String getName() {
        return name;
    }
}
